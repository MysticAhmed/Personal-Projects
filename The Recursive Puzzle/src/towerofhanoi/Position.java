package towerofhanoi;

// -------------------------------------------------------------------------
/**
 * There are always three poles in the Towers of Hanoi puzzle, and so each Tower
 * is associated with one of three positions: LEFT, MIDDLE, or RIGHT.
 * 
 * @author ahmedabdelhady
 * @version Mar 17, 2024
 */

public enum Position
{
    /**
     * LEFT position
     */
    LEFT,
    /**
     * MIDDLE position
     */
    MIDDLE,
    /**
     * RIGHT position
     */
    RIGHT,
    /**
     * DEFAULT position
     */
    DEFAULT;
}
